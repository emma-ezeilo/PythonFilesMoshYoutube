############### Python Primitive data types #########
# STRING DATA TYPE
# STRING IS USED WHEN UNIQUENESS IS THE KEY
emp_first_name = 'Victor'
emp_last_name : str = 'Chidera'
emp_phone_num = '08034077345'

# INTEGER DATA TYPE
# INTEGER IS USED WHEN MANIPULATION IS REQUIRED AND PRECISION IS NOT THE KEY
emp_age : int = 23
emp_certification = 25

# FLOAT DATA TYPE
# FLOAT IS USED WHEN MANIPULATION IS REQUIRED AND PRECISION IS THE KEY
emp_salary : float = 230_000_000.0
emp_height = 25.78

# BOOLEAN DATA TYPE
# BOOLEAN IS USED WHEN YES OR NO IS A RESPONSE
emp_is_married : bool = True
emp_is_comfirmed = False

# NONE DATA TYPE
# NONE IS USED WHEN THERE IS NO RESPONSE
emp_acc_balance = None

# BYTES DATA TYPE
# BYTES IS USED FOR NETWORK DATA STREAMING
emp_stream : bytes = 'youtube.com/redeem.mp4'
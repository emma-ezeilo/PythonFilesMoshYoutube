# For loops in python example 1
for item in "Python":
    print(item)
print("\n")

# Example 2
for item in ['Mosh', 'John', 'Sarah']:
    print(item)
print("\n")

# Example 3
for item in [1, 2, 3, 4]:
    print(item)
print("\n")

# Example 4
for item in range(10):
    print(item)
print("\n")

# Example 5
for item in range(5, 10):
    print(item)
print("\n")

# Example 6
for item in range(5, 10, 2):
    print(item)

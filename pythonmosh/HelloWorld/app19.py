x = 10
# Now python interpreter sets x to 10
x = x + 3
# And then because x is 10,it does 10 + 3 and stores it in x
print(x)
# It then prints x which is 13

# We can also use augmented assignment operators also known as enhanced assignment operators
x = 10
x+=3
print(x)
# or we can subtract
x-=5
print(x) # This will print 8

# We can also multiply
x*=3
print(x) # This will print 24 